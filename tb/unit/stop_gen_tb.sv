`timescale 1ns/1ps

module stop_gen_tb;

    // ============================================================
    // CONTROL
    // ============================================================

    logic stop_enable;

    // ============================================================
    // DRIVE CONTROL
    // ============================================================

    logic sda_drive_low;
    logic scl_drive_low;

    // ============================================================
    // BUS WIRES
    // ============================================================

    wire sda;
    wire scl;

    // ============================================================
    // PULL-UP RESISTORS
    // ============================================================

    pullup(sda);
    pullup(scl);

    // ============================================================
    // DUT
    // ============================================================

    stop_gen dut (

        .stop_enable    (stop_enable),

        .sda_drive_low  (sda_drive_low),
        .scl_drive_low  (scl_drive_low)

    );

    // ============================================================
    // FORCE SDA LOW INITIALLY
    // ------------------------------------------------------------
    // Creates pre-STOP condition.
    // ============================================================

    assign sda = (sda_drive_low) ? 1'b0 :
                 (stop_enable)   ? 1'bz :
                                   1'b0;

    assign scl = (scl_drive_low) ? 1'b0 : 1'bz;

    // ============================================================
    // TEST SEQUENCE
    // ============================================================

    initial begin

        $display("\n=================================");
        $display("STOP GENERATION TEST");
        $display("=================================\n");

        stop_enable = 0;

        #50;

        // ========================================================
        // GENERATE STOP
        // ========================================================

        stop_enable = 1;

        #50;

        stop_enable = 0;

        #50;

        $display("\n=================================");
        $display("SIMULATION COMPLETE");
        $display("=================================\n");

        $finish;

    end

endmodule
