`timescale 1ns/1ps

module stop_gen_tb;

    // ============================================================
    // CONTROL
    // ============================================================

    logic stop_enable;

    // ============================================================
    // BUS WIRES
    // ============================================================

    wire sda;
    wire scl;

    // ============================================================
    // BUS OBSERVATION
    // ============================================================

    logic sda_in;
    logic scl_in;

    // ============================================================
    // DRIVE CONTROL
    // ============================================================

    logic sda_drive_low;
    logic scl_drive_low;

    // ============================================================
    // PULL-UP RESISTORS
    // ============================================================

    pullup(sda);
    pullup(scl);

    // ============================================================
    // DUT
    // ============================================================

    stop_gen dut (

        .stop_enable    (stop_enable),

        .scl_in         (scl_in),
        .sda_in         (sda_in),

        .sda_drive_low  (sda_drive_low),
        .scl_drive_low  (scl_drive_low)

    );

    // ============================================================
    // OPEN-DRAIN BUS MODEL
    // ============================================================

    assign sda = (sda_drive_low) ? 1'b0 : 1'bz;
    assign scl = (scl_drive_low) ? 1'b0 : 1'bz;

    // ============================================================
    // OBSERVE BUS
    // ============================================================

    assign sda_in = sda;
    assign scl_in = scl;

    // ============================================================
    // INITIAL BUS STATE
    // ------------------------------------------------------------
    // Create:
    // SDA LOW
    // SCL HIGH
    //
    // so STOP can occur.
    // ============================================================

    initial begin

        force sda = 1'b0;

        #30;

        release sda;

    end

    // ============================================================
    // TEST SEQUENCE
    // ============================================================

    initial begin

        $display("\n======================================");
        $display("STOP CONDITION GENERATION TEST");
        $display("======================================\n");

        stop_enable = 0;

        #50;

        // ========================================================
        // GENERATE STOP
        // ========================================================

        stop_enable = 1;

        #50;

        stop_enable = 0;

        #50;

        $display("\n======================================");
        $display("SIMULATION COMPLETE");
        $display("======================================\n");

        $finish;

    end

endmodule