`timescale 1ns/1ps

module i2c_line_ctrl_tb;

    // ============================================================
    // DRIVE CONTROL SIGNALS
    // ------------------------------------------------------------
    // Simulate controller intent.
    // ============================================================

    logic sda_drive_low;
    logic scl_drive_low;

    // ============================================================
    // SHARED BUS WIRES
    // ------------------------------------------------------------
    // Represent physical PCB traces.
    // ============================================================

    wire sda;
    wire scl;

    // ============================================================
    // OBSERVED BUS VALUES
    // ============================================================

    logic sda_in;
    logic scl_in;

    // ============================================================
    // PULL-UP RESISTOR MODELS
    // ------------------------------------------------------------
    // Simulate external I2C pull-up resistors.
    //
    // Real hardware typically uses:
    //  - 1kΩ to 10kΩ
    // depending on:
    //  - bus capacitance
    //  - speed
    //  - power requirements
    // ============================================================

    pullup(sda);
    pullup(scl);

    // ============================================================
    // DEVICE UNDER TEST
    // ============================================================

    i2c_line_ctrl dut (

        .sda_drive_low (sda_drive_low),
        .scl_drive_low (scl_drive_low),

        .sda            (sda),
        .scl            (scl),

        .sda_in         (sda_in),
        .scl_in         (scl_in)

    );

    // ============================================================
    // MONITOR
    // ------------------------------------------------------------
    // Continuously display bus activity.
    // ============================================================

    initial begin

        $monitor(
            "[TIME=%0t] "
            "SDA_drive_low=%0b "
            "SCL_drive_low=%0b "
            "SDA=%0b "
            "SCL=%0b",
            $time,
            sda_drive_low,
            scl_drive_low,
            sda,
            scl
        );

    end

    // ============================================================
    // TEST SEQUENCE
    // ============================================================

    initial begin

        $display("\n========================================");
        $display("I2C OPEN-DRAIN LINE CONTROLLER TEST");
        $display("========================================\n");

        // ========================================================
        // INITIAL STATE
        // --------------------------------------------------------
        // Both released.
        //
        // Expected:
        //      SDA = 1
        //      SCL = 1
        // ========================================================

        sda_drive_low = 0;
        scl_drive_low = 0;

        #20;

        // ========================================================
        // PULL SDA LOW
        // ========================================================

        sda_drive_low = 1;

        #20;

        // ========================================================
        // RELEASE SDA
        // ========================================================

        sda_drive_low = 0;

        #20;

        // ========================================================
        // PULL SCL LOW
        // ========================================================

        scl_drive_low = 1;

        #20;

        // ========================================================
        // RELEASE SCL
        // ========================================================

        scl_drive_low = 0;

        #20;

        // ========================================================
        // PULL BOTH LOW
        // ========================================================

        sda_drive_low = 1;
        scl_drive_low = 1;

        #20;

        // ========================================================
        // RELEASE BOTH
        // ========================================================

        sda_drive_low = 0;
        scl_drive_low = 0;

        #20;

        $display("\n========================================");
        $display("SIMULATION COMPLETE");
        $display("========================================\n");

        $finish;

    end

endmodule