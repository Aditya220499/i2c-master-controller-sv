`timescale 1ns/1ps

module start_gen_tb;

    // ============================================================
    // CONTROL
    // ============================================================

    logic start_enable;

    // ============================================================
    // DRIVE CONTROL
    // ============================================================

    logic sda_drive_low;
    logic scl_drive_low;

    // ============================================================
    // BUS WIRES
    // ============================================================

    wire sda;
    wire scl;

    // ============================================================
    // PULL-UP RESISTORS
    // ============================================================

    pullup(sda);
    pullup(scl);

    // ============================================================
    // DUT
    // ============================================================

    start_gen dut (

        .start_enable (start_enable),

        .sda_drive_low(sda_drive_low),
        .scl_drive_low(scl_drive_low)

    );

    // ============================================================
    // OPEN-DRAIN BUS MODEL
    // ============================================================

    assign sda = (sda_drive_low) ? 1'b0 : 1'bz;
    assign scl = (scl_drive_low) ? 1'b0 : 1'bz;

    // ============================================================
    // TEST SEQUENCE
    // ============================================================

    initial begin

        $display("\n=================================");
        $display("START GENERATION TEST");
        $display("=================================\n");

        // ========================================================
        // INITIAL IDLE BUS
        // ========================================================

        start_enable = 0;

        #50;

        // ========================================================
        // GENERATE START
        // ========================================================

        start_enable = 1;

        #50;

        // ========================================================
        // RELEASE CONTROL
        // ========================================================

        start_enable = 0;

        #50;

        $display("\n=================================");
        $display("SIMULATION COMPLETE");
        $display("=================================\n");

        $finish;

    end

endmodule
