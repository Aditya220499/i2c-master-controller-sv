`timescale 1ns/1ps

module i2c_tx_pipeline_tb;

    // ============================================================
    // CLOCK / RESET
    // ============================================================

    logic clk;
    logic rst_n;

    // ============================================================
    // TX CONTROL
    // ============================================================

    logic tx_start;

    logic [7:0] tx_data;

    // ============================================================
    // SLAVE ACK EMULATION
    // ============================================================

    logic slave_ack_drive_low;

    // ============================================================
    // DUT OUTPUTS
    // ============================================================

    logic tx_active;

    logic ack_received;

    logic sda;

    // ============================================================
    // DUT
    // ============================================================

    i2c_tx_pipeline_top dut (

        .clk                    (clk),
        .rst_n                  (rst_n),

        .tx_start               (tx_start),

        .tx_data                (tx_data),

        .slave_ack_drive_low    (slave_ack_drive_low),

        .tx_active              (tx_active),

        .ack_received           (ack_received),

        .sda                    (sda)

    );

    // ============================================================
    // CLOCK
    // ============================================================

    initial begin

        clk = 0;

        forever #5 clk = ~clk;

    end

    // ============================================================
    // RESET
    // ============================================================

    initial begin

        rst_n = 0;

        #20;

        rst_n = 1;

    end

    // ============================================================
    // TEST
    // ============================================================

    initial begin

        tx_start = 0;

        tx_data  = 8'hA5;

        slave_ack_drive_low = 0;

        wait(rst_n);

        // ========================================================
        // START TRANSACTION
        // ========================================================

        @(negedge clk);

        tx_start = 1;

        @(posedge clk);

        @(negedge clk);

        tx_start = 0;

        // ========================================================
        // WAIT FOR ACK PHASE
        // ========================================================

        wait(dut.ack_phase == 1);

        // ========================================================
        // SLAVE SENDS ACK
        // ========================================================

        slave_ack_drive_low = 1;

        // Hold ACK during ACK clock

        #80;

        slave_ack_drive_low = 0;

        // ========================================================
        // WAIT FOR TX COMPLETE
        // ========================================================

        wait(tx_active == 0);

        #100;

        $display("\n=================================");
        $display("TX PIPELINE INTEGRATION COMPLETE");
        $display("=================================\n");

        $finish;

    end

endmodule
