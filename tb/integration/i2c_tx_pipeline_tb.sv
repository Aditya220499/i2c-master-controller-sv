`timescale 1ns/1ps

module i2c_tx_pipeline_tb;

    // ============================================================
    // CLOCK / RESET
    // ============================================================

    logic clk;
    logic rst_n;

    // ============================================================
    // TX CONTROL
    // ============================================================

    logic tx_start;

    logic [7:0] tx_data;

    // ============================================================
    // SLAVE ACK EMULATION
    // ------------------------------------------------------------
    // 1:
    //      slave pulls SDA LOW
    //
    // 0:
    //      slave releases SDA
    // ============================================================

    logic slave_ack_drive_low;

    // ============================================================
    // DUT OUTPUTS
    // ============================================================

    logic tx_active;

    logic ack_received;

    logic sda;

    // ============================================================
    // DUT
    // ============================================================

    i2c_tx_pipeline_top dut (

        .clk                    (clk),
        .rst_n                  (rst_n),

        .tx_start               (tx_start),

        .tx_data                (tx_data),

        .slave_ack_drive_low    (slave_ack_drive_low),

        .tx_active              (tx_active),

        .ack_received           (ack_received),

        .sda                    (sda)

    );

    // ============================================================
    // CLOCK GENERATION
    // ============================================================

    initial begin

        clk = 0;

        forever #5 clk = ~clk;

    end

    // ============================================================
    // RESET GENERATION
    // ============================================================

    initial begin

        rst_n = 0;

        #20;

        rst_n = 1;

    end

    // ============================================================
    // MAIN TEST
    // ============================================================

    initial begin

        // ========================================================
        // INITIAL VALUES
        // ========================================================

        tx_start = 0;

        tx_data  = 8'hA5;

        slave_ack_drive_low = 0;

        // ========================================================
        // WAIT RESET COMPLETE
        // ========================================================

        wait(rst_n);

        // ========================================================
        // START TRANSACTION
        // ========================================================

        @(negedge clk);

        tx_start = 1'b1;

        @(posedge clk);

        @(negedge clk);

        tx_start = 1'b0;

        // ========================================================
        // WAIT UNTIL ACK PHASE
        // ========================================================

        wait(dut.ack_phase == 1'b1);

        // ========================================================
        // WAIT FOR ACK CLOCK HIGH PHASE
        // --------------------------------------------------------
        // Slave must respond during ACK clock
        // ========================================================

        wait(dut.scl_high_phase == 1'b1);

        // ========================================================
        // SLAVE SENDS ACK
        // --------------------------------------------------------
        // ACK means:
        // slave pulls SDA LOW
        // ========================================================

        slave_ack_drive_low = 1'b1;

        // ========================================================
        // HOLD ACK THROUGH SAMPLE POINT
        // ========================================================

        wait(dut.sample_pulse == 1'b1);

        @(posedge clk);

        // ========================================================
        // RELEASE SDA AFTER ACK
        // ========================================================

        slave_ack_drive_low = 1'b0;

        // ========================================================
        // WAIT FOR TRANSACTION COMPLETE
        // ========================================================

        wait(tx_active == 1'b0);

        // ========================================================
        // EXTRA TIME FOR CLEAN WAVEFORM
        // ========================================================

        #200;

        // ========================================================
        // TEST COMPLETE
        // ========================================================

        $display("\n=================================");
        $display("TX PIPELINE INTEGRATION COMPLETE");
        $display("=================================\n");

        $finish;

    end

endmodule
