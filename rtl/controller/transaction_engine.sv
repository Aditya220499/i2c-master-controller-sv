`timescale 1ns/1ps

module transaction_engine (

    input  logic clk,
    input  logic rst_n,

    // ============================================================
    // TRANSACTION CONTROL
    // ============================================================

    input  logic start_transaction,

    // ============================================================
    // HANDSHAKES
    // ============================================================

    input  logic start_done,
    input  logic stop_done,

    input  logic byte_done,

    input  logic ack_received,

    // ============================================================
    // CONTROL OUTPUTS
    // ============================================================

    output logic start_enable,
    output logic stop_enable,

    output logic tx_start,

    // ============================================================
    // STATUS
    // ============================================================

    output logic busy,
    output logic done,

    // ============================================================
    // DEBUG
    // ============================================================

    output logic [3:0] debug_state

);

    // ============================================================
    // FSM STATES
    // ============================================================

    typedef enum logic [3:0] {

        IDLE           = 4'd0,
        GEN_START      = 4'd1,
        SEND_ADDR      = 4'd2,
        WAIT_ADDR_ACK  = 4'd3,
        SEND_DATA      = 4'd4,
        WAIT_DATA_ACK  = 4'd5,
        GEN_STOP       = 4'd6,
        DONE_STATE     = 4'd7

    } state_t;

    // ============================================================
    // STATE REGISTERS
    // ============================================================

    state_t current_state;
    state_t next_state;

    // ============================================================
    // STATE REGISTER
    // ============================================================

    always_ff @(posedge clk or negedge rst_n) begin

        if (!rst_n)
            current_state <= IDLE;
        else
            current_state <= next_state;

    end

    // ============================================================
    // NEXT STATE LOGIC
    // ============================================================

    always_comb begin

        next_state = current_state;

        case(current_state)

            // ====================================================
            // IDLE
            // ====================================================

            IDLE: begin

                if(start_transaction)
                    next_state = GEN_START;

            end

            // ====================================================
            // START
            // ====================================================

            GEN_START: begin

                if(start_done)
                    next_state = SEND_ADDR;

            end

            // ====================================================
            // SEND ADDRESS
            // ====================================================

            SEND_ADDR: begin

                if(byte_done)
                    next_state = WAIT_ADDR_ACK;

            end

            // ====================================================
            // ADDRESS ACK
            // ====================================================

            WAIT_ADDR_ACK: begin

                if(ack_received)
                    next_state = SEND_DATA;
                else
                    next_state = GEN_STOP;

            end

            // ====================================================
            // SEND DATA
            // ====================================================

            SEND_DATA: begin

                if(byte_done)
                    next_state = WAIT_DATA_ACK;

            end

            // ====================================================
            // DATA ACK
            // ====================================================

            WAIT_DATA_ACK: begin

                next_state = GEN_STOP;

            end

            // ====================================================
            // STOP
            // ====================================================

            GEN_STOP: begin

                if(stop_done)
                    next_state = DONE_STATE;

            end

            // ====================================================
            // DONE
            // ====================================================

            DONE_STATE: begin

                next_state = IDLE;

            end

        endcase
    end

    // ============================================================
    // OUTPUT LOGIC
    // ============================================================

    always_comb begin

        // ========================================================
        // DEFAULTS
        // ========================================================

        start_enable = 0;
        stop_enable  = 0;

        tx_start     = 0;

        busy         = 1;
        done         = 0;

        case(current_state)

            // ====================================================
            // IDLE
            // ====================================================

            IDLE: begin

                busy = 0;

            end

            // ====================================================
            // START
            // ====================================================

            GEN_START: begin

                start_enable = 1;

            end

            // ====================================================
            // SEND ADDRESS
            // ====================================================

            SEND_ADDR: begin

                tx_start = 1;

            end

            // ====================================================
            // SEND DATA
            // ====================================================

            SEND_DATA: begin

                tx_start = 1;

            end

            // ====================================================
            // STOP
            // ====================================================

            GEN_STOP: begin

                stop_enable = 1;

            end

            // ====================================================
            // DONE
            // ====================================================

            DONE_STATE: begin

                busy = 0;
                done = 1;

            end

        endcase
    end

    // ============================================================
    // DEBUG
    // ============================================================

    assign debug_state = current_state;

endmodule
