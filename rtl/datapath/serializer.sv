`timescale 1ns/1ps

module serializer (

    input  logic       clk,
    input  logic       rst_n,

    // ============================================================
    // LOAD CONTROL
    // ============================================================

    input  logic       load,

    // ============================================================
    // SHIFT CONTROL
    // ============================================================

    input  logic       shift_enable,

    // ============================================================
    // PARALLEL INPUT DATA
    // ============================================================

    input  logic [7:0] tx_data,

    // ============================================================
    // SERIAL OUTPUT
    // ============================================================

    output logic       serial_bit,

    // ============================================================
    // BYTE COMPLETE
    // ============================================================

    output logic       byte_done,

    // ============================================================
    // DEBUG
    // ============================================================

    output logic [7:0] debug_shift_reg,
    output logic [3:0] debug_bit_count

);

    // ============================================================
    // INTERNALS
    // ============================================================

    logic [7:0] shift_reg;

    logic [3:0] bit_count;

    // ============================================================
    // SERIALIZER LOGIC
    // ============================================================

    always_ff @(posedge clk or negedge rst_n) begin

        if (!rst_n) begin

            shift_reg <= 8'h00;

            bit_count <= 4'd0;

            byte_done <= 1'b0;

        end
        else begin

            // ====================================================
            // DEFAULT
            // ====================================================

            byte_done <= 1'b0;

            // ====================================================
            // LOAD NEW BYTE
            // ====================================================

            if (load) begin

                shift_reg <= tx_data;

                bit_count <= 4'd8;

            end

            // ====================================================
            // SHIFT OPERATION
            // ====================================================

            else if (shift_enable && bit_count != 0) begin

                // ================================================
                // SHIFT REGISTER
                // ================================================

                shift_reg <= {
                    shift_reg[6:0],
                    1'b0
                };

                // ================================================
                // FINAL BIT SHIFT
                // ================================================

                if (bit_count == 4'd1) begin

                    byte_done <= 1'b1;

                end

                // ================================================
                // DECREMENT COUNTER
                // ================================================

                bit_count <= bit_count - 1'b1;

            end
        end
    end

    // ============================================================
    // SERIAL OUTPUT
    // ============================================================

    assign serial_bit = shift_reg[7];

    // ============================================================
    // DEBUG
    // ============================================================

    assign debug_shift_reg = shift_reg;

    assign debug_bit_count = bit_count;

endmodule
