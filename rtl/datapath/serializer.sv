`timescale 1ns/1ps

module serializer (

    input  logic       clk,
    input  logic       rst_n,

    // ============================================================
    // LOAD CONTROL
    // ------------------------------------------------------------
    // Loads new transmit byte into serializer.
    // ============================================================

    input  logic       load,

    // ============================================================
    // SHIFT CONTROL
    // ------------------------------------------------------------
    // Advances serializer by one bit.
    // ============================================================

    input  logic       shift_enable,

    // ============================================================
    // PARALLEL INPUT DATA
    // ============================================================

    input  logic [7:0] tx_data,

    // ============================================================
    // CURRENT SERIAL OUTPUT BIT
    // ------------------------------------------------------------
    // MSB-first serialization.
    // ============================================================

    output logic       serial_bit,

    // ============================================================
    // BYTE COMPLETE
    // ------------------------------------------------------------
    // Asserted after final bit shifted.
    // ============================================================

    output logic       byte_done,

    // ============================================================
    // DEBUG
    // ============================================================

    output logic [7:0] debug_shift_reg,
    output logic [2:0] debug_bit_count

);

    // ============================================================
    // SHIFT REGISTER
    // ============================================================

    logic [7:0] shift_reg;

    // ============================================================
    // BIT COUNTER
    // ============================================================

    logic [2:0] bit_count;

    // ============================================================
    // SERIALIZER LOGIC
    // ============================================================

    always_ff @(posedge clk or negedge rst_n) begin

        if (!rst_n) begin

            shift_reg <= 8'h00;

            bit_count <= 3'd0;

            byte_done <= 1'b0;

        end
        else begin

            // ====================================================
            // DEFAULT
            // ====================================================

            byte_done <= 1'b0;

            // ====================================================
            // LOAD NEW BYTE
            // ====================================================

            if (load) begin

                shift_reg <= tx_data;

                bit_count <= 3'd7;

            end

            // ====================================================
            // SHIFT OPERATION
            // ====================================================

            else if (shift_enable) begin

                // ================================================
                // Shift LEFT
                //
                // Next bit moves into MSB position.
                // ================================================

                shift_reg <= {
                    shift_reg[6:0],
                    1'b0
                };

                // ================================================
                // BIT TRACKING
                // ================================================

                if (bit_count == 3'd0) begin

                    byte_done <= 1'b1;

                end
                else begin

                    bit_count <= bit_count - 1'b1;

                end
            end
        end
    end

    // ============================================================
    // SERIAL OUTPUT BIT
    // ------------------------------------------------------------
    // Always transmit current MSB.
    // ============================================================

    assign serial_bit = shift_reg[7];

    // ============================================================
    // DEBUG
    // ============================================================

    assign debug_shift_reg = shift_reg;

    assign debug_bit_count = bit_count;

endmodule